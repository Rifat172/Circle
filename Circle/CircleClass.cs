using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Circle
{
    class CircleClass
    {
        public int X;
        public int Y;
        public int radius;
        public int diameter;

        public CircleClass(int x, int y, int radius_, int diameter_)
        {
            X = x;
            Y = y;
            radius = radius_;
            diameter = diameter_;
        }
    }
}
